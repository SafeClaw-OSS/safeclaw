//! `sc up` / `down` / `restart` / `logs` — user-level daemon lifecycle.
//!
//! Two backends, one mental model ("bring the daemon up in the background,
//! auto-restart on crash, state under `$HOME`, no sudo"):
//!   - **Linux:** a per-user systemd unit at
//!     `~/.config/systemd/user/safeclaw.service`, driven by `systemctl --user`.
//!   - **macOS:** a LaunchAgent at
//!     `~/Library/LaunchAgents/pro.safeclaw.daemon.plist`, driven by
//!     `launchctl` (RunAtLoad + KeepAlive = the systemd unit's start +
//!     `Restart=on-failure`). Logs go to `~/.safeclaw/daemon.log`.
//! Other platforms fall back to `sc serve` (foreground).
//!
//! Why user-level (not system-level)?
//! - No sudo required — the daemon runs as the same user that called `sc start`.
//! - Vault state lives under `$HOME/.safeclaw/`, owned by the same user.
//! - Encapsulates "this user's SafeClaw daemon" cleanly. Multi-user hosts
//!   work too, each user gets their own daemon.
//!
//! Operators running SafeClaw as a system service (root-owned, the dev VM
//! shape) should write their own `/etc/systemd/system/safeclaw.service`
//! and not touch these commands.

use std::process::Command as ProcCommand;
use crate::config::LogsArgs;

const UNIT_BASENAME: &str = "safeclaw.service";
/// First line of every unit we generate. The idempotency guard refuses to
/// overwrite a unit that lacks this marker (a hand-written one) unless
/// `--force` is passed.
const UNIT_MARKER: &str = "# Generated by safeclaw — do not edit; re-run `sc up` to refresh.";
/// Stable substring used for *detection* (the "is this our unit?" guard).
/// The full `UNIT_MARKER`'s trailing hint has drifted across versions
/// (`sc custodian start` → `sc up`), so detecting on the whole string
/// orphaned units written by older builds — `sc` stopped recognizing its
/// own unit and refused to manage it. Detect on this invariant prefix; all
/// safeclaw-generated units (past and present) start the marker line with it.
const UNIT_MARKER_DETECT: &str = "# Generated by safeclaw";

// ── macOS: launchd LaunchAgent (the per-user, no-sudo analog of the Linux
//    user-systemd unit above — RunAtLoad + KeepAlive gives the same
//    "background, auto-restart on crash, state under $HOME" semantics). ──
#[cfg(target_os = "macos")]
const LAUNCHD_LABEL: &str = "pro.safeclaw.daemon";
/// XML-comment marker mirroring `UNIT_MARKER` — the same idempotency guard
/// (never clobber a hand-written plist at our path unless `--force`).
#[cfg(target_os = "macos")]
const PLIST_MARKER_DETECT: &str = "Generated by safeclaw";

#[cfg(target_os = "macos")]
fn launchd_plist_path() -> Result<std::path::PathBuf, String> {
    let home = dirs::home_dir().ok_or("no home dir")?;
    Ok(home
        .join("Library/LaunchAgents")
        .join(format!("{LAUNCHD_LABEL}.plist")))
}

#[cfg(target_os = "macos")]
fn launchd_log_path() -> Result<std::path::PathBuf, String> {
    let home = dirs::home_dir().ok_or("no home dir")?;
    Ok(home.join(".safeclaw").join("daemon.log"))
}

/// Escape a string for inclusion in plist XML text (paths / env values).
#[cfg(target_os = "macos")]
fn xml_escape(s: &str) -> String {
    s.replace('&', "&amp;")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
        .replace('"', "&quot;")
        .replace('\'', "&apos;")
}

#[cfg(target_os = "macos")]
fn run_launchctl(args: &[&str], action: &str) -> Result<(), String> {
    let output = ProcCommand::new("launchctl")
        .args(args)
        .output()
        .map_err(|e| format!("spawn launchctl: {}", e))?;
    if !output.status.success() {
        let stderr = String::from_utf8_lossy(&output.stderr);
        return Err(format!("launchctl {} failed: {}", action, stderr.trim()));
    }
    Ok(())
}

#[cfg(target_os = "linux")]
pub async fn run_start_systemd(force: bool) -> Result<(), String> {
    let bin = std::env::current_exe()
        .map_err(|e| format!("can't find current binary path: {}", e))?;
    let bin_str = bin.to_string_lossy().to_string();

    // Collect any SAFECLAW_* env vars set in the calling shell — these
    // become Environment= lines in the unit. The user controls config
    // by setting env vars *before* `sc start`; flags are ignored here
    // (they'd be invisible after install, which is worse).
    let mut env_lines = Vec::new();
    for (k, v) in std::env::vars() {
        // Don't leak the agent-facing SAFECLAW_API_KEY into the daemon's
        // env — the daemon reads its api-key from the FILE `~/.safeclaw/api-key`
        // (provisioned below), and a stray/foreign value in the operator's
        // shell must not be adopted as the broker key.
        if k.starts_with("SAFECLAW_") && k != "SAFECLAW_API_KEY" {
            // systemd unit syntax: quote the value to allow spaces/=.
            // Escape any " inside.
            let v_escaped = v.replace('"', "\\\"");
            env_lines.push(format!("Environment=\"{}={}\"", k, v_escaped));
        }
    }
    // Broker auth is the cloud-synced agent-key hash-set (agent ≡ api-key),
    // refreshed at runtime by `crate::sync` — there's no machine-local key file
    // to provision here. See [[project_vault_agent_architecture_2026_06_25]].
    let env_block = if env_lines.is_empty() { String::new() } else { format!("{}\n", env_lines.join("\n")) };

    let unit = format!(
        "{UNIT_MARKER}\n\
         [Unit]\n\
         Description=SafeClaw daemon (user)\n\
         After=network.target\n\
         \n\
         [Service]\n\
         Type=simple\n\
         {env_block}\
         ExecStart={bin} serve\n\
         Restart=on-failure\n\
         RestartSec=5\n\
         \n\
         [Install]\n\
         WantedBy=default.target\n",
        env_block = env_block,
        bin = bin_str,
    );

    let unit_dir = dirs::config_dir()
        .ok_or("can't locate user config dir (XDG_CONFIG_HOME)")?
        .join("systemd/user");
    std::fs::create_dir_all(&unit_dir)
        .map_err(|e| format!("create {}: {}", unit_dir.display(), e))?;
    let unit_path = unit_dir.join(UNIT_BASENAME);
    // Idempotency guard: never silently clobber a unit we didn't generate.
    // A unit lacking the (version-stable) safeclaw marker prefix is
    // hand-written; refuse unless --force. We match `UNIT_MARKER_DETECT`
    // (not the full `UNIT_MARKER`) so units written by older builds — whose
    // marker tail differs — are still recognized as ours.
    if !force {
        if let Ok(existing) = std::fs::read_to_string(&unit_path) {
            if !existing.contains(UNIT_MARKER_DETECT) {
                return Err(format!(
                    "{} already exists and was not generated by safeclaw \
                     (no marker). Refusing to overwrite. Re-run with `--force` \
                     to replace it, or `sc up` to just start the existing unit.",
                    unit_path.display()
                ));
            }
        }
    }
    std::fs::write(&unit_path, &unit)
        .map_err(|e| format!("write {}: {}", unit_path.display(), e))?;
    // Unit may embed SAFECLAW_ADMIN_KEY and other secrets via
    // Environment= lines. Tighten to user-only so other accounts on a
    // multi-user host can't read them.
    #[cfg(unix)]
    {
        use std::os::unix::fs::PermissionsExt;
        std::fs::set_permissions(&unit_path, std::fs::Permissions::from_mode(0o600))
            .map_err(|e| format!("chmod 0600 {}: {}", unit_path.display(), e))?;
    }

    run_systemctl(&["daemon-reload"], "daemon-reload")?;
    run_systemctl(&["enable", "--now", UNIT_BASENAME], "enable+start")?;

    eprintln!("✓ daemon enabled and running ({})", unit_path.display());
    eprintln!("  binary:   {}", bin_str);
    if !env_lines.is_empty() {
        eprintln!("  env:      {} SAFECLAW_* var(s) embedded", env_lines.len());
    }
    eprintln!("  broker:   requires an agent key (synced from your account); mint one with `sc agent add` or the dashboard's \"Connect a new agent\"");
    eprintln!();
    eprintln!("  next: `sc login --pair-token <token>` to connect this daemon to your vault (token from the dashboard)");
    eprintln!("        `sc logs -f` to tail, `sc down` to stop, `sc restart` to reload");
    Ok(())
}

#[cfg(target_os = "macos")]
pub async fn run_start_systemd(force: bool) -> Result<(), String> {
    let bin = std::env::current_exe()
        .map_err(|e| format!("can't find current binary path: {}", e))?;
    let bin_str = bin.to_string_lossy().to_string();

    // Same policy as the systemd path: the daemon inherits SAFECLAW_* set in
    // the calling shell (so `SAFECLAW_PORT=<p> sc up` moves the port and it
    // sticks across restarts), EXCEPT the agent-facing SAFECLAW_API_KEY.
    let mut env_entries = Vec::new();
    for (k, v) in std::env::vars() {
        if k.starts_with("SAFECLAW_") && k != "SAFECLAW_API_KEY" {
            env_entries.push(format!(
                "    <key>{}</key><string>{}</string>",
                xml_escape(&k),
                xml_escape(&v)
            ));
        }
    }
    let env_block = if env_entries.is_empty() {
        String::new()
    } else {
        format!(
            "  <key>EnvironmentVariables</key>\n  <dict>\n{}\n  </dict>\n",
            env_entries.join("\n")
        )
    };

    let log_path = launchd_log_path()?;
    let log_str = log_path.to_string_lossy().to_string();
    // KeepAlive{SuccessfulExit=false} = restart on crash, not on clean exit —
    // the launchd analog of the unit's `Restart=on-failure`.
    let plist = format!(
        "<?xml version=\"1.0\" encoding=\"UTF-8\"?>\n\
         <!-- {marker} -->\n\
         <!DOCTYPE plist PUBLIC \"-//Apple//DTD PLIST 1.0//EN\" \"http://www.apple.com/DTDs/PropertyList-1.0.dtd\">\n\
         <plist version=\"1.0\">\n\
         <dict>\n\
         \x20 <key>Label</key>\n\
         \x20 <string>{label}</string>\n\
         \x20 <key>ProgramArguments</key>\n\
         \x20 <array>\n\
         \x20   <string>{bin}</string>\n\
         \x20   <string>serve</string>\n\
         \x20 </array>\n\
         {env_block}\
         \x20 <key>RunAtLoad</key>\n\
         \x20 <true/>\n\
         \x20 <key>KeepAlive</key>\n\
         \x20 <dict><key>SuccessfulExit</key><false/></dict>\n\
         \x20 <key>ProcessType</key>\n\
         \x20 <string>Background</string>\n\
         \x20 <key>StandardOutPath</key>\n\
         \x20 <string>{log}</string>\n\
         \x20 <key>StandardErrorPath</key>\n\
         \x20 <string>{log}</string>\n\
         </dict>\n\
         </plist>\n",
        marker = UNIT_MARKER.trim_start_matches("# "),
        label = LAUNCHD_LABEL,
        bin = xml_escape(&bin_str),
        env_block = env_block,
        log = xml_escape(&log_str),
    );

    let plist_path = launchd_plist_path()?;
    let dir = plist_path.parent().ok_or("bad LaunchAgents path")?;
    std::fs::create_dir_all(dir).map_err(|e| format!("create {}: {}", dir.display(), e))?;
    // Ensure the log's parent (~/.safeclaw) exists so launchd can open it.
    if let Some(logdir) = log_path.parent() {
        std::fs::create_dir_all(logdir)
            .map_err(|e| format!("create {}: {}", logdir.display(), e))?;
    }
    // Idempotency guard (mirrors the systemd path): refuse to overwrite a
    // plist we didn't generate unless --force.
    if !force {
        if let Ok(existing) = std::fs::read_to_string(&plist_path) {
            if !existing.contains(PLIST_MARKER_DETECT) {
                return Err(format!(
                    "{} already exists and was not generated by safeclaw. \
                     Refusing to overwrite. Re-run with `--force` to replace it.",
                    plist_path.display()
                ));
            }
        }
    }
    std::fs::write(&plist_path, &plist)
        .map_err(|e| format!("write {}: {}", plist_path.display(), e))?;
    #[cfg(unix)]
    {
        use std::os::unix::fs::PermissionsExt;
        std::fs::set_permissions(&plist_path, std::fs::Permissions::from_mode(0o600))
            .map_err(|e| format!("chmod 0600 {}: {}", plist_path.display(), e))?;
    }

    let plist_arg = plist_path.to_string_lossy().to_string();
    // Reload cleanly: drop any prior registration (ignore "not loaded"), then
    // load -w (enables + RunAtLoad starts it).
    let _ = run_launchctl(&["unload", &plist_arg], "unload");
    run_launchctl(&["load", "-w", &plist_arg], "load")?;

    eprintln!("✓ daemon installed and running ({})", plist_path.display());
    eprintln!("  binary:   {}", bin_str);
    if !env_entries.is_empty() {
        eprintln!("  env:      {} SAFECLAW_* var(s) embedded", env_entries.len());
    }
    eprintln!("  logs:     {}", log_str);
    eprintln!();
    eprintln!("  next: `sc login --pair-token <token>` to connect this daemon to your vault (token from the dashboard)");
    eprintln!("        `sc logs -f` to tail, `sc down` to stop, `sc restart` to reload");
    Ok(())
}

#[cfg(not(any(target_os = "linux", target_os = "macos")))]
pub async fn run_start_systemd(_force: bool) -> Result<(), String> {
    Err("`sc up` (service mode) is Linux/macOS-only. Use `sc serve` to run in this process.".into())
}

/// Idempotent "ensure the daemon is running" — the skill's lazy-start target.
/// Starts the user-level unit only if it isn't already active; never rewrites
/// the unit (so it can't clobber config or race a concurrent start). If no
/// unit is installed yet, it tells the user to run `sc up` once.
#[cfg(target_os = "linux")]
pub fn run_ensure_running() -> Result<(), String> {
    // Already active? Nothing to do — this is the common case the skill hits
    // every session, so keep it cheap and silent-ish.
    let active = ProcCommand::new("systemctl")
        .args(["--user", "is-active", "--quiet", UNIT_BASENAME])
        .status()
        .map(|s| s.success())
        .unwrap_or(false);
    if active {
        return Ok(());
    }
    // Not active. If the unit exists, just start it (no rewrite). Otherwise
    // the daemon was never installed as a service — guide the user.
    ensure_unit_installed().map_err(|_| {
        "daemon is not running and no service is installed — run `sc up` once to install it".to_string()
    })?;
    run_systemctl(&["start", UNIT_BASENAME], "start")?;
    eprintln!("✓ daemon started");
    Ok(())
}

#[cfg(target_os = "macos")]
pub fn run_ensure_running() -> Result<(), String> {
    let plist_path = launchd_plist_path()?;
    let plist_arg = plist_path.to_string_lossy().to_string();
    // Already loaded? `launchctl list <label>` exits 0 when registered, and
    // KeepAlive keeps the process itself alive — treat loaded as running.
    let loaded = ProcCommand::new("launchctl")
        .args(["list", LAUNCHD_LABEL])
        .status()
        .map(|s| s.success())
        .unwrap_or(false);
    if loaded {
        return Ok(());
    }
    if !plist_path.exists() {
        return Err(
            "daemon is not running and no LaunchAgent is installed — run `sc up` once to install it"
                .to_string(),
        );
    }
    run_launchctl(&["load", "-w", &plist_arg], "load")?;
    eprintln!("✓ daemon started");
    Ok(())
}

#[cfg(not(any(target_os = "linux", target_os = "macos")))]
pub fn run_ensure_running() -> Result<(), String> {
    Err("`sc up` (service mode) is Linux/macOS-only. Use `sc serve` to run in this process.".into())
}

